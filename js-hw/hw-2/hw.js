/*
    Есть две перменные a и b
    в них хранится число, но в виде строки
    необходиммо, с помощью функции alert, вывести на экран сообщение:
    'Сумма чисел <число-a> и <число-b> равна: <сумма>'
*/
function houseWork(argA, argB) {
    const a = argA;
    const b = argB;    
    // начало здесь
    c = parseInt(a) + parseInt(b);
    alert("Сумма чисел " + a + " и " + b + " равна: " +  c);
    
} // код не должен выходить за закрывающую скобку
